import "dotenv/config";
import { writeFileSync } from "node:fs";
import { getCompetitor, listDigests } from "../lib/digest";
import { OWNER_WORKSPACE_ID } from "../lib/db";

async function main() {
  const out: Array<{
    id: number;
    competitor: { id: number; name: string; domain: string };
    period_start: string;
    period_end: string;
    urgency: string;
    body: unknown;
    created_at: string;
  }> = [];

  for (const d of await listDigests(OWNER_WORKSPACE_ID)) {
    const c = await getCompetitor(OWNER_WORKSPACE_ID, d.competitor_id);
    if (!c) continue;
    out.push({
      id: d.id,
      competitor: { id: c.id, name: c.name, domain: c.domain },
      period_start: d.period_start,
      period_end: d.period_end,
      urgency: d.urgency,
      body: d.body,
      created_at: d.created_at,
    });
  }

  const ts = `// Auto-generated by \`tsx src/bin/export-demo.ts\`. Real digests baked in
// so /demo works on serverless deployments without persistent DB.

import type { DigestBody, Urgency } from "./types";

export interface DemoDigest {
  id: number;
  competitor: { id: number; name: string; domain: string };
  period_start: string;
  period_end: string;
  urgency: Urgency;
  body: DigestBody;
  created_at: string;
}

export const DEMO_DIGESTS: DemoDigest[] = ${JSON.stringify(out, null, 2)};
`;

  writeFileSync("./src/lib/demo-data.ts", ts);
  console.log(`✓ exported ${out.length} digests to src/lib/demo-data.ts`);
}

main().catch((e) => {
  console.error(e);
  process.exit(1);
});
